import type { SchoolGrade } from '@/server/db/schema';
import type { PaginateReturn } from '@/server/utils/paginate';

import { initContract } from '@ts-rest/core';
import { z } from 'zod';

import {
  SchoolGradeCreateBodySchema,
  SchoolGradeFindManyQuerySchema,
  SchoolGradeFindUniqueSchema,
  SchoolGradeUpdateBodySchema,
} from '@/schemas/school-grade';

const c = initContract();

export const schoolGrade = c.router(
  {
    findUnique: {
      method: 'GET',
      path: `/:id`,
      pathParams: z.object({ id: z.coerce.number() }),
      query: SchoolGradeFindUniqueSchema.optional(),
      responses: {
        200: c.type<SchoolGrade>(),
        400: c.type<{ message: string }>(),
        404: c.type<{ message: string }>(),
        500: c.type<{ message: string }>(),
      },
    },
    findMany: {
      method: 'GET',
      path: `/`,
      query: SchoolGradeFindManyQuerySchema,
      responses: {
        200: c.type<PaginateReturn<SchoolGrade>>(),
        400: c.type<{ message: string }>(),
        500: c.type<{ message: string }>(),
      },
    },
    create: {
      method: 'POST',
      path: `/`,
      body: SchoolGradeCreateBodySchema,
      responses: {
        201: c.type<SchoolGrade>(),
        400: c.type<{ message: string }>(),
        500: c.type<{ message: string }>(),
      },
    },
    update: {
      method: 'PUT',
      path: `/:id`,
      pathParams: z.object({ id: z.coerce.number() }),
      body: SchoolGradeUpdateBodySchema,
      responses: {
        200: c.type<SchoolGrade>(),
        400: c.type<{ message: string }>(),
        404: c.type<{ message: string }>(),
        500: c.type<{ message: string }>(),
      },
    },
    delete: {
      method: 'DELETE',
      path: `/:id`,
      pathParams: z.object({ id: z.coerce.number() }),
      body: null,
      responses: {
        200: c.type<SchoolGrade>(),
        400: c.type<{ message: string }>(),
        404: c.type<{ message: string }>(),
        500: c.type<{ message: string }>(),
      },
    },
  },
  { pathPrefix: '/school-grades' }
);
