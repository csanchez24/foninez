import type { EducationLevel } from '@/server/db/schema';
import type { PaginateReturn } from '@/server/utils/paginate';

import { initContract } from '@ts-rest/core';
import { z } from 'zod';

import {
  EducationLevelCreateBodySchema,
  EducationLevelFindManyQuerySchema,
  EducationLevelFindUniqueSchema,
  EducationLevelUpdateBodySchema,
} from '@/schemas/education-level';

const c = initContract();

export const educationLevel = c.router(
  {
    findUnique: {
      method: 'GET',
      path: `/:id`,
      pathParams: z.object({ id: z.coerce.number() }),
      query: EducationLevelFindUniqueSchema.optional(),
      responses: {
        200: c.type<EducationLevel>(),
        400: c.type<{ message: string }>(),
        404: c.type<{ message: string }>(),
        500: c.type<{ message: string }>(),
      },
    },
    findMany: {
      method: 'GET',
      path: `/`,
      query: EducationLevelFindManyQuerySchema,
      responses: {
        200: c.type<PaginateReturn<EducationLevel>>(),
        400: c.type<{ message: string }>(),
        500: c.type<{ message: string }>(),
      },
    },
    create: {
      method: 'POST',
      path: `/`,
      body: EducationLevelCreateBodySchema,
      responses: {
        201: c.type<EducationLevel>(),
        400: c.type<{ message: string }>(),
        500: c.type<{ message: string }>(),
      },
    },
    update: {
      method: 'PUT',
      path: `/:id`,
      pathParams: z.object({ id: z.coerce.number() }),
      body: EducationLevelUpdateBodySchema,
      responses: {
        200: c.type<EducationLevel>(),
        400: c.type<{ message: string }>(),
        404: c.type<{ message: string }>(),
        500: c.type<{ message: string }>(),
      },
    },
    delete: {
      method: 'DELETE',
      path: `/:id`,
      pathParams: z.object({ id: z.coerce.number() }),
      body: null,
      responses: {
        200: c.type<EducationLevel>(),
        400: c.type<{ message: string }>(),
        404: c.type<{ message: string }>(),
        500: c.type<{ message: string }>(),
      },
    },
  },
  { pathPrefix: '/education-levels' }
);
