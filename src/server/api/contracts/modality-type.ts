import type { ModalityType } from '@/server/db/schema';
import type { PaginateReturn } from '@/server/utils/paginate';

import { initContract } from '@ts-rest/core';
import { z } from 'zod';

import {
  ModalityTypeCreateBodySchema,
  ModalityTypeFindManyQuerySchema,
  ModalityTypeFindUniqueSchema,
  ModalityTypeUpdateBodySchema,
} from '@/schemas/modality-type';

const c = initContract();

export const modalityType = c.router(
  {
    findUnique: {
      method: 'GET',
      path: `/:id`,
      pathParams: z.object({ id: z.coerce.number() }),
      query: ModalityTypeFindUniqueSchema.optional(),
      responses: {
        200: c.type<ModalityType>(),
        400: c.type<{ message: string }>(),
        404: c.type<{ message: string }>(),
        500: c.type<{ message: string }>(),
      },
    },
    findMany: {
      method: 'GET',
      path: `/`,
      query: ModalityTypeFindManyQuerySchema,
      responses: {
        200: c.type<PaginateReturn<ModalityType>>(),
        400: c.type<{ message: string }>(),
        500: c.type<{ message: string }>(),
      },
    },
    create: {
      method: 'POST',
      path: `/`,
      body: ModalityTypeCreateBodySchema,
      responses: {
        201: c.type<ModalityType>(),
        400: c.type<{ message: string }>(),
        500: c.type<{ message: string }>(),
      },
    },
    update: {
      method: 'PUT',
      path: `/:id`,
      pathParams: z.object({ id: z.coerce.number() }),
      body: ModalityTypeUpdateBodySchema,
      responses: {
        200: c.type<ModalityType>(),
        400: c.type<{ message: string }>(),
        404: c.type<{ message: string }>(),
        500: c.type<{ message: string }>(),
      },
    },
    delete: {
      method: 'DELETE',
      path: `/:id`,
      pathParams: z.object({ id: z.coerce.number() }),
      body: null,
      responses: {
        200: c.type<ModalityType>(),
        400: c.type<{ message: string }>(),
        404: c.type<{ message: string }>(),
        500: c.type<{ message: string }>(),
      },
    },
  },
  { pathPrefix: '/modality-types' }
);
