import type { Plan } from '@/server/db/schema';
import type { PaginateReturn } from '@/server/utils/paginate';

import { initContract } from '@ts-rest/core';
import { z } from 'zod';

import {
  PlanCreateBodySchema,
  PlanFindManyQuerySchema,
  PlanFindUniqueSchema,
  PlanUpdateBodySchema,
  PlanUpdateStatusBodySchema,
} from '@/schemas/plan';

const c = initContract();

export const plan = c.router(
  {
    findUnique: {
      method: 'GET',
      path: `/:id`,
      pathParams: z.object({ id: z.coerce.number() }),
      query: PlanFindUniqueSchema.optional(),
      responses: {
        200: c.type<Plan>(),
        400: c.type<{ message: string }>(),
        404: c.type<{ message: string }>(),
        500: c.type<{ message: string }>(),
      },
    },
    findMany: {
      method: 'GET',
      path: `/`,
      query: PlanFindManyQuerySchema,
      responses: {
        200: c.type<PaginateReturn<Plan>>(),
        400: c.type<{ message: string }>(),
        500: c.type<{ message: string }>(),
      },
    },
    create: {
      method: 'POST',
      path: `/`,
      body: PlanCreateBodySchema,
      responses: {
        201: c.type<Plan>(),
        400: c.type<{ message: string }>(),
        500: c.type<{ message: string }>(),
      },
    },
    update: {
      method: 'PUT',
      path: `/:id`,
      pathParams: z.object({ id: z.coerce.number() }),
      body: PlanUpdateBodySchema,
      responses: {
        200: c.type<Plan>(),
        400: c.type<{ message: string }>(),
        404: c.type<{ message: string }>(),
        500: c.type<{ message: string }>(),
      },
    },
    updateStatus: {
      method: 'PATCH',
      path: `/:id/status`,
      pathParams: z.object({ id: z.coerce.number() }),
      body: PlanUpdateStatusBodySchema,
      responses: {
        200: c.type<Plan>(),
        400: c.type<{ message: string }>(),
        404: c.type<{ message: string }>(),
        500: c.type<{ message: string }>(),
      },
    },
    delete: {
      method: 'DELETE',
      path: `/:id`,
      pathParams: z.object({ id: z.coerce.number() }),
      body: null,
      responses: {
        200: c.type<Plan>(),
        400: c.type<{ message: string }>(),
        404: c.type<{ message: string }>(),
        500: c.type<{ message: string }>(),
      },
    },
  },
  { pathPrefix: '/plans' }
);
